package Serie2TipiDiDato.EsempiSlides_ClasseMath;
public class Test {
    public static void main(String[] args) {
        System.out.println("cos(pi/2) = " + Math.cos(Math.PI / 2));
        System.out.println("sin(pi/2) = " + Math.sin(Math.PI / 2));
        System.out.println("tan(pi/4) = " + Math.tan(Math.PI / 4));

        System.out.println("acos(1) = " + Math.acos(1));
        System.out.println("asin(.75) = " + Math.asin(.75));
        System.out.println("atan(.5) = " + Math.atan(.5));

        System.out.println("exp(1.0) = " + Math.exp(1.0));
        System.out.println("exp(10.0) = " + Math.exp(10.0));
        System.out.println("exp(0.0) = " + Math.exp(0.0));

        System.out.println("log(1.0) = " + Math.log(1.0));
        System.out.println("log(e) = " + Math.log(Math.E));
        System.out.println("log10(10.0) = " + Math.log10(10.0));

        System.out.println("pow(2.0, 2.0) = " + Math.pow(2.0, 2.0));
        System.out.println("pow(10.0, 3.5) = " + Math.pow(10.0, 3.5));
        System.out.println("pow(8, -1) = " + Math.pow(8, -1));
    }
}
