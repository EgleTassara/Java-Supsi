public class OperatoriSuInt {
    public static void main(String[] args) {
        int a = 15;
        int b = 24;

        int c1 = b - a + 7;
        int c2 = b - a - 4;
        int c3 = b % a / 2;
        int c4 = b * a / 2;
        int c5 = b / (2 * a);

        System.out.println("c1 = " + c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
        System.out.println("c4 = " + c4);
        System.out.println("c5 = " + c5);
    }
}
