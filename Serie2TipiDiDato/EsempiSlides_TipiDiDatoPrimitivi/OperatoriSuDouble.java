public class OperatoriSuDouble {
    public static void main(String[] args) {
        double a = 15;
        double b = 24;

        double c1 = b - a + 7;
        double c2 = b - a - 4;
        double c3 = b % a / 2;
        double c4 = b * a / 2;
        double c5 = b / (2 * a);

        System.out.println("c1 = " + c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
        System.out.println("c4 = " + c4);
        System.out.println("c5 = " + c5);
    }
}
