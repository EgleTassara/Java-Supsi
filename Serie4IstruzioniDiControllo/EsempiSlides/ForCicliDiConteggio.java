public class ForCicliDiConteggio {
    public static void main(String[] args) {

        System.out.println("Conteggio crescente");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("\nConteggio decrescente");
        for (int i = 9; i >= 0; i--) {
            System.out.println(i);
        }

    }
}
